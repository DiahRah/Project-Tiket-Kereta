package enkapsulasi;

/**
 *
 * @author ASUS
 */
public class enkapsulasiDaftarTiket {
    private String idtiket, nik, nama, kodekereta, jurusan, keberangkatan, nomorkursi;

    public String getIdtiket() {
        return idtiket;
    }

    public void setIdtiket(String idtiket) {
        this.idtiket = idtiket;
    }

    public String getNik() {
        return nik;
    }

    public void setNik(String nik) {
        this.nik = nik;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getKodekereta() {
        return kodekereta;
    }

    public void setKodekereta(String kodekereta) {
        this.kodekereta = kodekereta;
    }

    public String getJurusan() {
        return jurusan;
    }

    public void setJurusan(String jurusan) {
        this.jurusan = jurusan;
    }

    public String getKeberangkatan() {
        return keberangkatan;
    }

    public void setKeberangkatan(String keberangkatan) {
        this.keberangkatan = keberangkatan;
    }

    public String getNomorkursi() {
        return nomorkursi;
    }

    public void setNomorkursi(String nomorkursi) {
        this.nomorkursi = nomorkursi;
    }
    
    
}
