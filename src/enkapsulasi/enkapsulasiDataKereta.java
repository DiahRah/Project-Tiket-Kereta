public class enkapsulasiDataKereta {
    private String kodekereta, namakereta, jurusan, keberangkatan, jenis, nomorkursi, harga;

    public String getKodekereta() {
        return kodekereta;
    }

    public void setKodekereta(String kodekereta) {
        this.kodekereta = kodekereta;
    }

    public String getNamakereta() {
        return namakereta;
    }

    public void setNamakereta(String namakereta) {
        this.namakereta = namakereta;
    }

    public String getJurusan() {
        return jurusan;
    }

    public void setJurusan(String jurusan) {
        this.jurusan = jurusan;
    }

    public String getKeberangkatan() {
        return keberangkatan;
    }

    public void setKeberangkatan(String keberangkatan) {
        this.keberangkatan = keberangkatan;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public String getNomorkursi() {
        return nomorkursi;
    }

    public void setNomorkursi(String nomorkursi) {
        this.nomorkursi = nomorkursi;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }
    
    
}
